function countProfit(shoppers) {
  var listBarang = [ ['Sepatu Stacattu', 1500000, 10],
                     ['Baju Zoro', 500000, 2],
                     ['Sweater Uniklooh', 175000, 1]
                   ];

    if (shoppers.length == 0) { //Jika tidak ada pembeli
        return "Tidak ada data pembeli yang terdeteksi"
    }

let result = listBarang.map(([product, price, leftOver]) => { //Membuat desain output agar seperti yang diinginkan
            return {
                product: product, //Nama produk
                shoppers: [], //Untuk menampung data
                leftOver: leftOver, //Sisa stock
                totalProfit: 0 //Profit awal
            }
 });          
     for (let {name, product, amount} of shoppers) { 
        let item = result.find(item => item.product === product) //Untuk mencari nama produk dalam objek dan mencocokkan hingga ketemu
        let productPrice = listBarang.find(([x]) => x === product) //Untuk mencari harga produk
        if (item && productPrice && item.leftOver >= amount) {
            item.shoppers.push(name); //Untuk push nama pembeli ke array shopper
            item.leftOver -= amount; //Mengurangi stok tersisa dengan jumlah barang yang dibeli
            item.totalProfit += amount * productPrice[1]; //menghitung profit
        }
     }

 return result;
}

